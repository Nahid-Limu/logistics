    public function get_zone_list_by_areaid($id)
    {
        $zone_list=DB::table('tbzone')
            ->leftjoin('users','tbzone.createdBy','=','users.id')
            ->leftjoin('tbarea','tbzone.areaId','=','tbarea.id')
            ->select('tbzone.*','users.name as createdby','tbarea.name as areaName')
            ->where([['tbzone.status','=',1],['tbzone.areaId','=',$id]])
            ->orderBy('tbzone.name', 'ASC')
            ->get();

        return view('ajax.get_zone_list_by_areaid',compact('zone_list'));
        
    }
